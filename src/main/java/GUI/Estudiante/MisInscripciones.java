    private void txtcomedorMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtcomedorMousePressed
        
        Usuario usuarioActivo = Sesion.getUsuarioActual();
        Conexion conn = new Conexion();
        boolean existeRegistroBeca = conn.verificarRegistroBeca(usuarioActivo.getUsername());
        System.out.println(existeRegistroBeca);
        
        if (!existeRegistroBeca){
            FormularioBeca formBeca = new FormularioBeca();
            formBeca.setVisible(true);
            formBeca.setLocationRelativeTo(null);
            JFrame frame = (JFrame) SwingUtilities.getWindowAncestor(this);
            if (frame != null) {
                frame.setVisible(false);
                frame.dispose();
            }
        }else{
            String estadoBeca = conn.estadoBeca(usuarioActivo.getUsername());
            javax.swing.JOptionPane.showMessageDialog(this, "Usted ya ha realizado su inscripci√≥n. \n ESTADO DE LA BECA COMEDOR: " + estadoBeca, "Estado Beca Comedor", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_txtcomedorMousePressed


