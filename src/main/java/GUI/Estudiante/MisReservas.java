    private void txtcomedorMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtcomedorMousePressed
        UsuarioVO usuarioActivo=IniciarSesion.cuenta;
        ReservaviandaDAO reserva= new ReservaviandaDAO();
        ArrayList<ReservaviandaVO> list = reserva.buscarReserva(usuarioActivo.getNombreUsuario());
         if(list.size()> 0){
            ReservaviandaVO re= new ReservaviandaVO();
            re=list.get(0);
             if(re.getCancelada().equals("si")){
                 CancelarVianda cancel = new CancelarVianda();
                cancel.setVisible(true);
                cancel.setLocationRelativeTo(null);
                 // Ocultar el JFrame actual
                 this.setVisible(false);
                 // Opcional: Cerrar el JFrame actual
                JFrame frame = (JFrame) SwingUtilities.getWindowAncestor(this);
                    if (frame != null) {
                        frame.setVisible(false);
                        frame.dispose();
                    } 

                 }else{
            javax.swing.JOptionPane.showMessageDialog(this, "Ya no puede cancelar","", javax.swing.JOptionPane.INFORMATION_MESSAGE);}
            }else{
            javax.swing.JOptionPane.showMessageDialog(this, "Usted no reservo en el dia de la fecha","", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
     
    }//GEN-LAST:event_txtcomedorMousePressed

