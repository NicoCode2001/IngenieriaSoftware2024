    private void jButtonGuardarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonGuardarMousePressed
        // TODO add your handling code here:
        //Verifico el tipo de archivo (Factura/Remito)
        if (opFactura.isSelected()){
            tipoArchivo = "Factura";
        }else if(opRemito.isSelected()){
            tipoArchivo = "Remito";
        }
        Date date = jDateChooser1.getDate(); //obtengo el valor del jDateChooser
        
        //Verifico que el usuario haya ingresado todas las opciones
        if(dirArchivo.equals("")){
            javax.swing.JOptionPane.showMessageDialog(this, 
            "¬°Error al cargar la factura!\nPor favor, cargue un archivo (*).", 
            "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }else if (date == null){
            javax.swing.JOptionPane.showMessageDialog(this, 
            "¬°Error al cargar la factura!\nPor favor, seleccione una fecha (*).", 
            "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }else if(tipoArchivo.equals("")){
            javax.swing.JOptionPane.showMessageDialog(this, 
            "¬°Error al cargar la factura!\nPor favor, seleccione una opcion(Remito o Factura) (*).", 
            "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }else{
            Conexion conn = new Conexion();
            Usuario usuarioActivo = Sesion.getUsuarioActual();
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy"); //doy formato a la fecha
            fecha = dateFormat.format(date);    
            if(tipoArchivo.equals("Factura")){
                conn.cargarFactura(usuarioActivo.getUsername(), dirArchivo, fecha, jTextArea1.getText());
                javax.swing.JOptionPane.showMessageDialog(this, "¬°Factura cargada con exito!.", "CARGA DE FACTURA", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }else{
                conn.cargarRemito( dirArchivo, fecha, jTextArea1.getText());
                javax.swing.JOptionPane.showMessageDialog(this, "¬°Remito cargado con exito!.", "CARGA DE REMITO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }
            PrincipalSecretario principalS = new PrincipalSecretario();
            principalS.setVisible(true);
            principalS.setLocationRelativeTo(null);
            this.setVisible(false);
            this.dispose();
        }
    }//GEN-LAST:event_jButtonGuardarMousePressed

